numero = int(input("Digite um número (1 a 7): "))

if numero == 1:
    print("Domingo")
if numero == 2:
    print("Segunda-feira")
if numero == 3:
    print("Terça-feira")
if numero == 4:
    print("Quarta-feira")
if numero == 5:
    print("Quinta-feira")
if numero == 6:
    print("Sexta-feira")
if numero == 7:
    print("Sábado")
